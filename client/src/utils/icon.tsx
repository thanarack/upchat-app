const GetIcon = ({ mode, name, className, size }: any) => {
  let getIcon;
  if (mode === 'outline') {
    getIcon = outLineIcon[name];
  }

  if (getIcon) {
    return (
      <i
        dangerouslySetInnerHTML={{ __html: getIcon }}
        className={classNames(
          {
            'app-icon': size === '',
            'app-icon-lg': size === 'lg',
            'app-icon-xl': size === 'xl',
          },
          className || undefined
        )}
      />
    );
  }

  return null;
};

GetIcon.defaultProps = {
  size: '',
};

export { GetIcon };
